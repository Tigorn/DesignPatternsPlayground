import Foundation


class CatChef {
    
    func roastedMice() -> String {
        return "Roasted Mice! 🐭 Don't worry, it has been sanitized."
    }
    
    func grilledHamster() -> String {
        return "Grilled Hamster! 🐹 Not everyone can stomach this, but there you go."
    }
    
    func catMilk() -> String {
        return "Cat Milk! 🍼🍼 This one's my favorite, you better not waste it! Meow!"
    }
    
}